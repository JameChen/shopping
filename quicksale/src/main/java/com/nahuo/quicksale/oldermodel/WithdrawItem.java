package com.nahuo.quicksale.oldermodel;

public class WithdrawItem {
    
    private double tradeMoney;
    private long withdrawNumber;
    private String tradeTime;
    private String finishTime;
    private String tradeState;
    private int tradeStateId;
    private String checkResult;
    
    
    public double getTradeMoney() {
        return tradeMoney;
    }
    public void setTradeMoney(double tradeMoney) {
        this.tradeMoney = tradeMoney;
    }
    public long getWithdrawNumber() {
        return withdrawNumber;
    }
    public void setWithdrawNumber(long withdrawNumber) {
        this.withdrawNumber = withdrawNumber;
    }
    public String getTradeTime() {
        return tradeTime;
    }
    public void setTradeTime(String tradeTime) {
        this.tradeTime = tradeTime;
    }
    public String getTradeState() {
        return tradeState;
    }
    public void setTradeState(String tradeState) {
        this.tradeState = tradeState;
    }
    public String getFinishTime() {
        return finishTime;
    }
    public void setFinishTime(String finishTime) {
        this.finishTime = finishTime;
    }
    public int getTradeStateId() {
        return tradeStateId;
    }
    public void setTradeStateId(int tradeStateId) {
        this.tradeStateId = tradeStateId;
    }
    public String getCheckResult() {
        return checkResult;
    }
    public void setCheckResult(String checkResult) {
        this.checkResult = checkResult;
    }
  
    
    
    

}
